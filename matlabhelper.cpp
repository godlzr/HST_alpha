Mat matlabHelper::nlfilter(const Mat & _im)
{
    Mat Lut = Mat(_im.rows, _im.cols, CV_64FC1, Scalar(0));
    for(int i = 0; i<_im.rows; i++)
    {
        for(int j = 0; j< _im.cols; j++)
        {
            if(_im.at<uchar>(i,j) == 0)
                Lut.at<double>(i,j) = 0;
            else
            {
                double sum = 0;
                for(int m =0; m<3; m++)
                {
                    for(int n = 0; n<3; n++)
                    {
                        int r = i-1+m, c = j-1+n;
                        if(r>=0&&r<_im.rows&&c>=0&&c<_im.cols)
                        {
                            sum += _im.at<uchar>(r,c);
                        }
                    }
                }
                Lut.at<double>(i,j) = sum-255;
            }
        }
    }
    return Lut;
}