void matlabHelper::ordfilt2(Mat _proj, int windsze, Mat windmat, Mat & _dilation)
{
    int row = _proj.rows;
    int col = _proj.cols;
    
    int head = floor(windsze/2);
    int end = windsze - head;
    
    for(int i = 0; i < col; i++)
    {
        int cur = i - head;
        double val = 0;
        for(int wd =0; wd < windsze; wd++, cur++)
        {
            if(cur<0 || cur >col)
                windmat.at<double>(0, wd) = val;
            else
            {
                val = _proj.at<double>(0, cur);
                windmat.at<double>(0, wd) = val;
            }
        }
        double minVal;
        double maxVal;
        Point minLoc;
        Point maxLoc;
        minMaxLoc( windmat, &minVal, &maxVal, &minLoc, &maxLoc );
        
        _dilation.at<double>(0,i) = maxVal;
        
    }
}
