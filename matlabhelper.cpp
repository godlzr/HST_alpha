Mat matlabHelper::gaussianKernal(double sigma, int size)
{
    Mat K, x, y;
    double siz = (size-1)/2;
    Range xrg, yrg;
    xrg.start = -siz; xrg.end = siz;
    yrg.start = -siz; yrg.end = siz;
    double std = sigma;
    meshgrid(xrg, yrg, x, y);
    Mat tmp1,tmp2,tmp3;
    cv::multiply(x, x, tmp1);

    cv::multiply(y, y, tmp2);

    cv::add(tmp1,tmp2,tmp3);
    tmp3.convertTo(tmp3, CV_64FC1);
    Mat arg(size, size, CV_64FC1);
    cv::divide( tmp3, (double)(-2*std*std), arg);

    Mat h(size, size, CV_64FC1);
    double sumh = 0;
    for(int i = 0; i < arg.rows; i++)
    {
        for(int j = 0; j < arg.cols; j++)
        {
            h.at<double>(i,j) = exp(arg.at<double>(i,j));
            sumh += h.at<double>(i,j);
        }
    }
    if( sumh != 0)
    {
        for(int i = 0; i < arg.rows; i++)
        {
            for(int j = 0; j < arg.cols; j++)
            {
                h.at<double>(i,j) = h.at<double>(i,j)/sumh;
            }
        }
    }
    return h;
}

void matlabHelper::meshgrid(const cv::Range &xgv, const cv::Range &ygv, cv::Mat &X, cv::Mat &Y)
{
    std::vector<int> t_x, t_y;
    for(int i = xgv.start; i <= xgv.end; i++) t_x.push_back(i);
    for(int j = ygv.start; j <= ygv.end; j++) t_y.push_back(j);

    cv::repeat(cv::Mat(t_x).t(), t_y.size(), 1, X);
    cv::repeat(cv::Mat(t_y), 1, t_x.size(), Y);
}

Mat matlabHelper::atan2Mat(Mat & a, Mat & b)
{
    int rows = a.rows;
     int cols = a.cols;
     Mat out (rows, cols, a.type());
     for(int i=0; i<rows; i++)
     {
         for(int j=0; j<cols; j++)
         {
             double ptra = a.at<double>(i,j);
             double ptrb = b.at<double>(i,j);
             out.at<double>(i,j) = atan2(ptra,ptrb);
         }
     }
     return out;
}

